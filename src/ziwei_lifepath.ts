/**
 * 根據 activePalace 與所在的 12 宮列表，
 * 動態計算並獲取三方四正的宮位實體與關聯資料。
 */
export function calculateSanFangSiZheng(
  activePalaceName: string,
  allPalaces: any[]
): {
  active: any;
  opposite: any;
  trineA: any;
  trineB: any;
  alignment: LifelongPalaceAlignment | undefined;
} {
  const activeIdx = allPalaces.findIndex((p) => p.name === activePalaceName);
  
  if (activeIdx === -1) {
    return {
      active: allPalaces[0],
      opposite: allPalaces[0],
      trineA: allPalaces[0],
      trineB: allPalaces[0],
      alignment: undefined
    };
  }
  
  const opposite = allPalaces[(activeIdx + 6) % 12];
  const trineA = allPalaces[(activeIdx + 4) % 12];
  const trineB = allPalaces[(activeIdx + 8) % 12];
  const alignment = LIFELONG_PALACE_ALIGNMENTS[activePalaceName];
  
  return {
    active: allPalaces[activeIdx],
    opposite,
    trineA,
    trineB,
    alignment
  };
}
